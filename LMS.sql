CREATE DATABASE LMS;
USE LMS;

-- MEMBUAT TABEL DAFTAR USER
CREATE TABLE daftar_user(
ID_USER INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
U_NAME VARCHAR (50) NOT NULL ,
TGL_LAHIR DATE,
PEKERJAAN VARCHAR (50),
ALAMAT VARCHAR (100)
);

-- MEMBUAT TABEL DAFTAR BUKU
CREATE TABLE daftar_buku(
ID_BUKU VARCHAR(50) NOT NULL PRIMARY KEY,
NAMA_BUKU VARCHAR (100) NOT NULL ,
KATEGORI VARCHAR(50),
STOCK INT
);

