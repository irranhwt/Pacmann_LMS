-- MEMBUAT DAFTAR PEMINJAMAN
CREATE TABLE daftar_pinjam(
ID_PINJAM INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
ID_USER INT NOT NULL,
ID_BUKU VARCHAR(50),
NAMA_USER VARCHAR(50),
NAMA_BUKU VARCHAR(100),
TANGGAL_PINJAM DATE,
TANGGAL_PENGEMBALIAN DATE,
FOREIGN KEY (ID_USER) REFERENCES daftar_user(ID_USER),
FOREIGN KEY (ID_BUKU) REFERENCES daftar_buku(ID_BUKU)
); 

 
